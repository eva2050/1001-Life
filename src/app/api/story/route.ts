export async function POST(request: NextRequest) {
  const body = await request.json();
  const {
    identityId = "",
    identityName,
    identityDesc,
    messages = [],
  } = body as {
    identityId: string;
    identityName: string;
    identityDesc: string;
    messages: { role: "user" | "assistant"; content: string }[];
  };

  if (!identityName) {
    return new Response(JSON.stringify({ error: "Missing identityName" }), { status: 400 });
  }

  const isFirstMessage = messages.length === 0;
  const persona = IDENTITY_PERSONAS[identityId] ?? DEFAULT_PERSONA(identityName, identityDesc);
  // 引子：只在第一次调用时附带，通过 SSE 的第一条特殊事件发出
  const openingHook = isFirstMessage
    ? (OPENING_HOOKS[identityId] ?? null)
    : null;

  const systemPrompt = `${NARRATIVE_PHILOSOPHY}

═══ 你的具体身份 ═══
${persona}`;

  const firstPrompt = `现在开始这一生。

不要介绍自己，直接从一个正在发生的事件进入——
有什么事情是悬而未决的，有什么人正在经过，有什么东西刚刚改变了。
开局就要有张力，让读者感到"后来怎样了？"。`;

  const conversationMessages = isFirstMessage
    ? [{ role: "user" as const, content: firstPrompt }]
    : messages;

  try {
    const stream = await ai.chat({
      model: "deepseek.v3.1",
      messages: [
        { role: "system", content: systemPrompt },
        ...conversationMessages,
      ],
      stream: true,
      max_tokens: 1000,
      temperature: 0.92,
    });

    const encoder = new TextEncoder();
    const readable = new ReadableStream({
      async start(controller) {
        try {
          // 引子先发出（type: "hook"），前端单独渲染
          if (openingHook) {
            controller.enqueue(encoder.encode(
              `data: ${JSON.stringify({ hook: openingHook })}\n\n`
            ));
          }
          for await (const chunk of stream) {
            const delta = chunk.choices[0]?.delta?.content ?? "";
            if (delta) {
              controller.enqueue(encoder.encode(`data: ${JSON.stringify({ text: delta })}\n\n`));
            }
          }
          controller.enqueue(encoder.encode("data: [DONE]\n\n"));
        } finally {
          controller.close();
        }
      },
    });

    return new Response(readable, {
      headers: {
        "Content-Type": "text/event-stream",
        "Cache-Control": "no-cache",
      },
    });
  } catch (err) {
    console.error("[story] AI error", err);
    return new Response(JSON.stringify({ error: "AI generation failed" }), { status: 500 });
  }
}
